<h1>Identify sounds</h1>
<a href="javascript:window.close();">Back to exercise</a><br><br>

This exercise tests your ability to place a character in the correct position in the consonant table by hearing them spoken.<br><br>

At the top of the page you will see an IPA table
with question marks everywhere where there is supposed to be a character. At the same time you will hear a sound spoken. Your task is to find the question mark which
is at the position where the sound spoken is supposed to be.<br><br>

When you believe you know which one it is, click on the question mark which is in the place of the character. If it is
incorrect, the system will tell you so, and the correct question mark will be replaced by the character in question. At the
same time a new sound will be spoken and the exercise goes on. If you need to hear the sound once more, there is a button
at the bottom enabling you to do so. 

