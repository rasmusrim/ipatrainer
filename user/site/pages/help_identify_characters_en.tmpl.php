<h1>Identify characters</h1>
<a href="javascript:window.close();">Back to exercise</a><br><br>

This exercise tests your ability to place a character in the correct position in the consonant table. Please refer to the image at bottom of the page in the following explanation.<br><br>

As you can see, there is an IPA character at the bottom (<font class="red">A</font>). Above it, you see the IPA table
with question marks everywhere where there is supposed to be a character. Your task is to find the question mark which
is at the position where the character in <font class="red">A</font> is supposed to be.<br><br>

In this case, the character
is a voiced alveolar tap or flap, so it should go to the question mark which <font class="red">B</font> is pointing
at. If you click on this question mark, you will get the message that your guess was correct, and a new IPA
character will appear in <font class="red">A</font> and the exercise goes on. If your guess is incorrect, 
you will be told so, and the alveolar tap will appear in the place it was supposed to be, so you can see what
you should have guessed. At the same time, a new IPA character will appear in <font class="red">A</font>, and the 
exercise goes on.<br><br>

