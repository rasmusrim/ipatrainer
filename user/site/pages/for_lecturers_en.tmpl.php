<h1>For lecturers:</h1>

<img src="<?PHP print(ROOT_URL); ?>/user/site/images/002.jpg" align="right" style="margin: 20px;">
This site offers you a way of teaching IPA characters to your students in a fun way. It gives you the opportunity to customise several IPA consonant tables to include only those characters your students need to learn. In this way, you can have one table for students of German, one for French and one for Swahili. When you become an administrator, you are given a username. You give this username to your students, and with it, they can enter your personal part of this site, and do exercises with the consonant tables you have made for them.<br><br> 

