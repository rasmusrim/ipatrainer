<h1>Identify places</h1>
<a href="javascript:window.close();">Back to exercise</a><br><br>

This exercise tests your ability to find the IPA character that should go into a certain place. Please refer to the image at bottom of the page in the following explanation.<br><br>

As you can see, all the IPA characters in this table are at the bottom in a random order (<font class="red">A</font>). Above them is the IPA table filled with only question marks, one of which being highlighted (<font class="red">B</font>).<br><br>

Your task is to identify the character that should go where the highlighted question mark is. You will then have to find that character in the list at the bottom and click on it. In this specific screenshot, the highlighted question mark is a retroflex approximant, which is the character pointed at by <font class="red">A</font>. Clicking on this character will tell you that the guess was correct, and another question mark will be highlighted so the exercise goes on. If it is incorrect, the highlighted question mark will be replaced by the correct character so you can see what you should have guessed, and at the same time a new question mark will be highlighted so the exercise goes on.<br><br>

