<h1>Donations:</h1>

As you probably have seen, everything on this site is free. I am not employed by anyone to work 
on it, but do so out of my love for both phonetics and programming. Unfortunately, developing
a site such as this requires time, and being a full time student and a father, there is a limit to how
much time I can spend on it.<br><br>

After IPA Trainer was launched on the 13th of February 2009, I have received lots of feedback from users of the site.
I have accumulated their suggestions in addition to my own ideas into the following todo list:

<ul>
	<li>Add some more consonants that I have been made aware of (/w/, affricates etc.).</li>
	<li>Add a possibility to practice vowels the same way as consonants</li>
	<li>Add a possibility to practice the difference between aspirated and unaspirated sounds.</li>
	<li>Make this site available in several languages.</li>
	<li>Build a discussion forum for both scholars and students.</li>
	<li>Build a database of user submitted recordings and their transcriptions for people to practice &quot;the real thing.&quot;</li>	
</ul>

I hope that you find this site useful as it is, and that you can see the usefulness of the above mentioned
ideas. If you do, I would like to ask you to consider a donation. If every person who has registered to customise
their own IPA tables donated $ 10, I could spend a week working on it this summer, having some of the features ready
for the start of the next term. Also, if you as a student feel that you owe some of your grade to IPA Trainer, a great
way of showing your gratitude would be to further its developement in this way.<br><br>

If you know of any foundations that further the cause of phonetics and linguistics to which a site like IPA Trainer
could apply, I would also be very happy if you <a href="<?PHP print(ROOT_URL); ?>/user/site/index.php?pageID=feedback">told me about it</a>.<br><br>

